#include <stdio.h>

/**
 * add - it adds two integers.
 * @a:first integer
 * @b:second integer.
 * Return: sum of two integers.
 */

int add(int a, int b)
{
	return (a + b);

}

/**
 * sub - it subtracts two integers.
 * @a:first integer
 * @b:second integer.
 * Return: difference of two integers.
 */

int sub(int a, int b)
{
	return (a - b);

}

/**
 * mul - it multiplies two integers.
 * @a:first integer
 * @b:second integer.
 * Return: product of two integers.
 */

int mul(int a, int b)
{
	return (a * b);

}

/**
 * mod - mod of two integers.
 * @a:first integer
 * @b:second integer.
 * Return: mod of two integers.
 */

int mod(int a, int b)
{
	return (a % b);

}

/**
 * div - it divides two integers.
 * @a:first integer
 * @b:second integer.
 * Return: division results of two integers.
 */

int div(int a, int b)
{
	return (a / b);
}
